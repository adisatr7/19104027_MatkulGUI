# Menggunakan Variabel
angka = 10

# range(max)
for i in range(angka):
    print("Angka ke : ")
    print(i)

# range(min, max)
for i in range(angka, 20):
    print("Angka ke : ")
    print(i)

# range(starting_point, ending_point, increment)
for i in range(angka, 1, -1):
    print(f"Angka ke : {i}")

# Array 
array = [1, 2, 3, 4]

for i in array:
    print(i)
